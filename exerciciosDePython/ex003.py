#crie um programa que leia dois números e mostre a soma entre eles.
print("Vamos calcular a soma de dois números")  #mensagem
num1 = float(input("Digite o primeiro número: "))   #recebendo o primeiro número
num2 = float(input("Digite o segundo número: "))    #recebendo o segundo número
print("A soma entre os números {} e {} é {}".format(num1,num2,num1+num2))   #exibindo o resultado na tela